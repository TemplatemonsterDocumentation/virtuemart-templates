<h2 class="item4">Configuring Modules</h2>

<p>The template may have some modules that are not included to the default VirtueMart/Joomla! installation and should be
    installed manually. The procedure is the same as described in step 1 of this document.</p>

<ol class="index-list">
    <li>In your Joomla! administration panel select "<strong>Extensions</strong> > <strong>Extensions Manager</strong>".
    </li>
    <li>Click the Browse button and select the extension package from the template <strong>"sources"</strong> folder.
    </li>
    <li>Click the "<strong>Upload &amp; Install</strong>" button.</li>
    <li>Go to "<strong>Extensions -> Module manager</strong>". The newly installed module should appear in the modules
        list.
    </li>
</ol>

<p>Open the module and set the configuration options according to the Modules Configuration table.</p>

<p>After all the necessary changes are applied, save the values of each module by clicking the "<strong>Save &amp;
        Close</strong>" button in the upper right corner.</p>

<p>You also need to upload the following folders to the root of your Joomla! installation:</p>

<ul class="marked-list">
    <li>sources\to_the_root_directory\administrator</li>
    <li>sources\to_the_root_directory\images</li>
    <li>sources\to_the_root_directory\media</li>
</ul>