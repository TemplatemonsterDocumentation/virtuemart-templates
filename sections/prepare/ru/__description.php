<h2 class="item2">Подготовка</h2>

<h6>Прежде, чем перейти к установке веб-сайта Joomla, нужно полностью подготовиться.
    Рекомендуется выполнить следующие подготовительные шаги:</h6>


<h4>Программное обеспечение</h4>

<p>Для полноценной работы с шаблоном Joomla мы рекомендуем скачать все требуемые
    приложения. Список необходимого программного обеспечения находится на странице
    предпросмотра шаблона. <br>
    Требования для различных шаблонов могут меняться, поэтому мы перечислим лишь
    основные:</p>

<ol class="index-list">
    <li>Прежде всего нужны правильные приложения для распаковывания защищенного паролем
        архива sources_#########.zip. Можно воспользоваться WinZip 9+ (Windows) и
        Stuffit Expander 10+ (Mac).
    </li>
    <li>Также может понадобиться программа Adobe Photoshop. Она используется для
        редактирования исходных файлов .PSD и необходима, если требуется изменить
        графический дизайн и изображения шаблона.
    </li>
    <li>Для редактирования исходного кода файлов шаблона нужно использовать редактор
        кода, например Adobe Dreamweaver, Notepad++, Sublime Text и т.п..
    </li>
    <li>Чтобы загрузить файлы на хостинг сервер, потребуется FTP менеджер, например
        Total Commander, FileZilla, CuteFTP и т.д..
    </li>
</ol>


<h4>Хостинг</h4>

<p>Поскольку Joomla CMS является приложением PHP/MySQL, необходимо подготовить хостинг
    сервер.</p>

<p>При наличии активного сервера, нужно проверить его на соответствие <a
        href="http://www.joomla.org/technical-requirements.html" target="_blank">требованиям</a><a
        href="http://www.joomla.org/technical-requirements.html" target="_blank">
        Joomla </a> и на готовность использовать веб-сайты Joomla.</p>

<p>Также можно установить Joomla на компьютер при помощи локального сервера. Для
    создания локального хостинг сервера необходимо использовать программное обеспечение
    <b>WAMP</b>, <b>AppServ</b>, <b>MAMP</b> и т.п. Любое из них устанавливается как
    обычная программа и поддерживает Joomla. </p>

