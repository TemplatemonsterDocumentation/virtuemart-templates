<h3>Установка Virtuemart3 (последней версии)</h3>

<p>Данная инструкция предполагает, что вы уже установили движок Joomla! 3. Вы также можете воспользоваться детальным
    видео туториалом и пошаговыми инструкциями <a href="/help/joomla-3-0-how-to-install-joomla-engine.html"
                                                  target="_blank">по установке системы управления содержимым Joomla.</a>
</p>
<ol class="index-list">
    <li><p>Войдите в админ панель Joomla, используя необходимые имя пользователя и пароль.</p></li>
    <li><p>Перейдите в меню <strong>Extensions </strong>&gt; <strong>Extension Manager</strong>
            (<strong>Расширения </strong>&gt;
            <strong>Менеджер расширений</strong>).</p></li>
    <li><p>Нажмите кнопку <strong>Browse (Обзор)</strong> и выберите файл
            <strong>sources/com_virtuemart.3.X.X.zip</strong>.
            Затем кликните кнопку <strong>Upload &amp; Install (Загрузить и установить)</strong>.</p>

        <p>Вы увидите следующее сообщение: "<strong>The installation was successful (Установка прошла успешно)</strong>".
        </p>

        <figure class="img-polaroid">
            <img src="img/install-virtue-mart.png" alt="">
        </figure>
    </li>
    <li><p>Теперь необходимо установить файл <strong>sources/com_virtuemart_ext_aio.3.X.X.zip</strong>. Нажмите кнопку
            <strong>Browse (Обзор)</strong> и выберите файл <strong>sources/com_virtuemart_ext_aio.3.X.X.zip</strong>.
            Затем
            кликните кнопку <strong>Upload &amp; Install (Загрузить и установить)</strong>. Когда установка будет
            завершена,
            вы
            увидите ссылку Virtuemart под меню <strong>Components (Компоненты)</strong>.</p>

        <figure class="img-polaroid">
            <img src="img/install-virtuemart-2.png" alt="">
        </figure>
    </li>
</ol>