<h3>Virtuemart3 installation (latest version)</h3>

<p>It is an assumption that you have already installed Joomla! 3. Feel free to check our detailed video and text
    instructions on <a href="/help/joomla-3-0-how-to-install-joomla-engine.html" target="_blank">how to install Joomla
        content management system</a></p>

<ol class="index-list">

    <li><p>Log into the Joomla admin panel with your username and password.</p></li>
    <li><p>Go to <strong>Extensions </strong>&gt; <strong>Extension Manager</strong>.</p></li>
    <li><p>Click the <strong>Browse </strong>button and select the <strong>sources/com_virtuemart.3.X.X.zip</strong>
            file.
            Then click the <strong>Upload &amp; Install </strong>button.</p>

        <p>You should see the message "<strong>The installation was successful</strong>".</p>

        <figure class="img-polaroid">
            <img src="img/install-virtue-mart.png" alt="">
        </figure>
    </li>
    <li><p>Install the other file called <strong>sources/com_virtuemart_ext_aio.3.X.X.zip</strong>. Click the
            <strong>Browse </strong>button and select the <strong>sources/com_virtuemart_ext_aio.3.X.X.zip</strong>
            file.
            Then
            click the <strong>Upload &amp; Install </strong>button. Once the installation is complete, you should see
            the
            Virtuemart link under the <strong>Components </strong>menu.</p>

        <figure class="img-polaroid">
            <img src="img/install-virtuemart-2.png" alt="">
        </figure>
    </li>
</ol>