<h3>Modules installation</h3>

<p>Joomla themes are provided with additional extensions: modules, plugins and
    components that are used to implement various functionality and features. All the
    additional extensions can be installed in a couple of clicks using the bundle
    package provided.</p>

<h6>The bundle archive is located in the 'sources' directory of your template package and
    called 'pkg_bundle.zip'. It can be easily installed, as any other extension or
    theme, through <b>Joomla admin panel > Extensions manager</b>. </h6>
<ol class="index-list">
    <li>Open the Joomla administration panel. From the top menu select
        '<b>Extensions</b>'. From the dropdown select '<b>Extensions Manager</b>'.
    </li>
    <li>Make sure the 'Install' section is selected in the left column.</li>
    <li>Click the 'Browse/Choose file' button and select the 'sources/pkg_bundle.zip'
        archive.
    </li>
    <li>Click the 'Upload and install' button to install all the modules.</li>
</ol>
