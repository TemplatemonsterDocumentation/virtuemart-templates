</figure>
<p>In case your theme contains some extensions which have not been included into '<b>pkg_bundle.zip</b>',
    you need to install each one separately. This can be done exactly as the
    'sources/pkg_bundle.zip' installation through the '<b>Extensions Manager</b>'
    interface.</p>

<p>The file names of extensions archives usually start with '<b>mod_</b>', '<b>com_</b>'
    or '<b>plg_</b>,' so you will recognize them easily. </p>