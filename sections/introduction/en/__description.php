<h2 class="item1">Introduction</h2>

<p>Thank you for purchasing a Joomla template. This documentation consists of several parts and shows you the entire
    process of how to set up and administer a Joomla Web site from scratch. We did our best to make this manual as clear
    and easy to follow as possible. </p>

<h3>What is Joomla CMS?</h3>

<p>Joomla CMS is an open source solution for building rich content Web sites. It enables you to build Web sites and
    powerful on-line applications and requires almost no technical skills or knowledge to manage. Many aspects,
    including its ease of use and extensibility, have made Joomla the most popular Web site software available. <a
        href="http://www.joomla.org/about-joomla.html" target="_blank">Learn More</a></p>


<h3>What is a Joomla template?</h3>

<p>A Joomla template is a skin for your Joomla CMS platform. In other words, you can easily change your Joomla Web
    site appearance installing a new template in a few easy steps. With all its simplicity a Joomla template is
    provided with all the necessary source files and you are free to edit or extend it the way you need.</p>

<h3>What is VirtueMart 3.0? </h3>

<p>VirtueMart is an Open Source E-Commerce solution (a component or a plug-in) for the Joomla! Content Management
    System.</p>

<p>It can be run as a Shopping Cart or in a Catalog mode. You need to install Joomla! first (as the "Framework") and
    then install & configure VirtueMart.<a href="http://virtuemart.net/features/what-is-virtuemart" target="_blank">Learn
        More</a></p>

