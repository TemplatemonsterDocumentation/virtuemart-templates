<h3>Image Swoop</h3>

<p>The "Image Swoop" module is an outstanding responsive slider developed as a
    combination of the default Joomla “Articles Newsflash” module and the free open
    source jQuery plugin “Camera Slideshow”. It works perfectly
    on desktop and iOs or Android mobile devices. Compatible with all modern browsers
    starting from IE8.</p>

<figure class="img-polaroid">
    <img src="img/camera-slideshow-3.jpg" alt="">
</figure>

<p>The "Image Swoop" module has various configuration options, you can access them by
    opening the Image Swoop module in the <b>Extensions > Module Manager</b> section of
    your Joomla administration panel. All the main "Camera Slideshow" jQuery plugin
    options can be configured in the module settings area. </p>

<p>Hover the mouse cursor over the option title to see its description. You can also
    learn more about Camera Slideshow jQuery plugin options on the <a
        href="http://www.pixedelic.com/plugins/camera/" target="_blank">official
        website</a></p>

