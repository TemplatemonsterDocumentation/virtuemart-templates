<h3>Articles Newsflash</h3>

<p>The Articles Newsflash (Advanced) module is an enhanced version of the default Joomla
    Articles Newsflash module. In addition to the default module features, it offers
    extra configuration options and Bootstrap based module templates.</p>

<figure class="img-polaroid">
    <img src="img/newsflash-adv-2.jpg" alt="">
</figure>

<h4>Additional Options</h4>

<p><b>Number of columns</b> - allows to display articles in multiple columns.</p>

<p><b>Custom Link</b> - allows you to display a custom link under the module content. It
    can be linked to the external URL or to some Joomla menu item. </p>

<p><b>Pretext</b> - allows to display custom text before the module content.</p>

<p><b>Published Date</b> - displays article published date. </p>

<p><b>Created by</b> - displays article author.</p>

<p><b>Show intro image</b>s - allows to display article intro image.</p>

<p><b>Intro image align</b> - allows to align intro images.</p>

