<h3>TM Instagram</h3>

<p>TM Instagram - module that allows to display "Instagram Widget"</p>

<figure class="img-polaroid">
    <img src="img/instagram-1.jpg" alt="">
</figure>

<p>Here you can see the main TM Instagram settings. Enter the Instagram profile name in
    the Instagram username field, enter the desired number of images in the Number of
    photos field, set the ID of your Instagram application in the the field called Your
    Instagram CLIENT ID. In order to get the ID, follow the link <a
        href="https://instagram.com/accounts/login/?next=%2Fdeveloper%2F"
        target="_blank" rel="nofollow">https://instagram.com/accounts/login/?next=%2Fdeveloper%2F</a>
    where you need to register an account. Then, follow this link <a
        href="http://instagram.com/developer/clients/manage/" target="_blank"
        rel="nofollow">http://instagram.com/developer/clients/manage/</a> and create
    the application. After that you will get your applications data where you will see
    the CLIENT ID.</p>

